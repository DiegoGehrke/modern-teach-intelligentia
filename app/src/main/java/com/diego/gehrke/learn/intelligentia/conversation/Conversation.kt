@Composable
fun ChatItemBubble(
    message: Message,
    isUserMe: Boolean
) {

    val backgroundBubbleColor = if (isUserMe) {
        MaterialTheme.colorScheme.primary
    } else {
        MaterialTheme.colorScheme.surfaceVariant
    }

    Column {
        Surface(
            color = backgroundBubbleColor,
            shape = ChatBubbleShape
        ) {
            ClickableMessage(
                message = message,
                isUserMe = isUserMe
            )
        }

        message.image?.let {
            ImageMessage(backgroundBubbleColor) { modifier, contentScale, contentDescription ->
                Image(
                    modifier = modifier,
                    painter = painterResource(id = it),
                    contentScale = contentScale,
                    contentDescription = contentDescription
                )
            }
        }

        message.imageUrl?.let {
            ImageMessage(backgroundBubbleColor) { modifier, contentScale, contentDescription ->
                Image(
                    modifier = modifier,
                    painter = rememberAsyncImagePainter(it),
                    contentScale = contentScale,
                    contentDescription = contentDescription
                )
            }
        }
    }
}

@Composable
fun ImageMessage(backgroundBubbleColor: Color, imageContent: @Composable (Modifier, ContentScale, String) -> Unit) {
    Spacer(modifier = Modifier.height(4.dp))
    Surface(
        color = backgroundBubbleColor,
        shape = ChatBubbleShape
    ) {
        imageContent(Modifier.size(300.dp), ContentScale.Fit, stringResource(id = R.string.attached_image))
    }
}

@Composable
fun ClickableMessage(
    message: Message,
    isUserMe: Boolean
) {
    val uriHandler = LocalUriHandler.current

    val styledMessage = messageFormatter(
        text = message.content,
        primary = isUserMe
    )

    ClickableText(
        text = styledMessage,
        style = MaterialTheme.typography.bodyLarge.copy(color = LocalContentColor.current),
        modifier = Modifier.padding(16.dp),
        onClick = {
            styledMessage
                .getStringAnnotations(start = it, end = it)
                .firstOrNull()
                ?.let { annotation ->
                    when (annotation.tag) {
                        SymbolAnnotationType.LINK.name -> uriHandler.openUri(annotation.item)
                        else -> Unit
                    }
                }
        }
    )
}
